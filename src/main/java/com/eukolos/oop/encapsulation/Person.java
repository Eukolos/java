package com.eukolos.oop.encapsulation;

public class Person {
    private String name;
    private String surname;
    private int age;
    private Long IdentityNumber;

    public Person(String name, String surname, int age, Long IdentityNumber) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.IdentityNumber = IdentityNumber;
    }


}
